package DAO;

/**
 *
 * @author USER
 */
public class OrderDetailDAO {
    int id;
    String name;
    String phone;
    String address;

    public OrderDetailDAO() {
    }

    public OrderDetailDAO(int id, String name, String phone, String address) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.address = address;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    @Override
    public String toString() {
        return "OrderDetailDAO{" + "id=" + id + ", name=" + name + ", phone=" + phone + ", address=" + address + '}';
    }
    
    
}
